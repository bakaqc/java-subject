package thuattoancsd.TreesTraversal;

public class Node {

    public int data;
    public Node left;
    public Node right;

    public Node() {
        this.left = null;
        this.left = null;
    }

    public Node(int data) {
        this.data = data;
        this.left = null;
        this.left = null;
    }
}
